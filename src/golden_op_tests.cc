void golden_op_run_1x1conv(void) {
  // Use serialized values
  tflite::ConvParams& params = *((tflite::ConvParams*)raw_1x1_conv_params);
  tflite::RuntimeShape& input_shape =
      *((tflite::RuntimeShape*)raw_1x1_input_shape);
  tflite::RuntimeShape& filter_shape =
      *((tflite::RuntimeShape*)raw_1x1_filter_shape);
  tflite::RuntimeShape& bias_shape =
      *((tflite::RuntimeShape*)raw_1x1_bias_shape);
  tflite::RuntimeShape& output_shape =
      *((tflite::RuntimeShape*)raw_1x1_output_shape);

  int8_t actual_output[2400];
  int8_t* expected_output = (int8_t*)raw_1x1_output_data;

  // Run convolution
  perf_reset_all_counters();
  perf_enable_counter(0);
  tflite::reference_integer_ops::ConvPerChannel(
      params, (const int32_t*)raw_1x1_output_multiplier,
      (const int32_t*)raw_1x1_output_shift, input_shape,
      (const int8_t*)raw_1x1_input_data, filter_shape,
      (const int8_t*)raw_1x1_filter_data, bias_shape,
      (const int32_t*)raw_1x1_bias_data, output_shape, actual_output);
  perf_disable_counter(0);
  printf("Cycle count: ");
  perf_print_value(perf_get_counter(0));
  puts("");

  // Check results are ok
  int fails = 0;
  for (size_t i = 0; i < 2400; i++) {
    if (actual_output[i] != expected_output[i]) {
      if (!fails) {
        printf("FAIL - first output tensor mismatch at %u\n", i);
        printf("       actual   = 0x%02x\n", actual_output[i] & 0xff);
        printf("       expected = 0x%02x\n", expected_output[i] & 0xff);
      }
      fails++;
    }
  }
  if (fails) {
    printf("FAIL - %d fails\n", fails);
  } else {
    puts("OK - output tensor matches");
  }
}
